
public class Ex1116_5 {

	public static void main(String[] args) {
		// do while문
		int num = 1;
		int sum = 0;
		do {
			sum += num;
			num++;
		} while (num <= 10);    //두 와일문은 세미콜론을 반드시 넣을 것
		System.out.println(sum + "======" + num);

	
		
		
		
		
		
		
		
		
		
		/* do {
		   실행문A;
		} while (조건식); */
		
		// do while은 while문에서 순서가 바뀌었다고 생각하면 쉬운데, 먼저{}안의 내용을 실행한 다음에 조건식을 확인하는 것이다.
			
	
	
	
	
	}

}
